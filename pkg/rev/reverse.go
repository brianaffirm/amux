package rev

// ReverseString returns s with its Unicode grapheme clusters reversed.
// It correctly handles multi-byte runes and combined emoji sequences.
func ReverseString(s string) string {
	runes := []rune(s)
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	return string(runes)
}
